// Lowercase, strip accents + non-alphanumeric, collapse runs of hyphens,
// truncate to 60 chars so URLs stay readable in SERPs.
export function slugify(input: string): string {
  return input
    .toLowerCase()
    .normalize('NFKD')
    .replace(/[̀-ͯ]/g, '')
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/^-+|-+$/g, '')
    .slice(0, 60)
    .replace(/-+$/, '');
}
